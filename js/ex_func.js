// 계산기
// 사칙연산
// 매개변수에 두개의 값을 집어넣고
// 값을 반환해줄꺼임

function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

console.log(add(3, 3));
console.log(sub(3, 3));
console.log(mul(3, 3));
console.log(div(3, 3));
